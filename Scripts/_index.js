jQuery_pocket(document).ready(function () {

    jQuery_pocket(".wpcf7-submit").parents("form").submit(function (e) {
        e.preventDefault();
        jQuery_pocket(".wpcf7-submit").click();
    })

    jQuery_pocket(".wpcf7-submit").click(function () {

        isError = false;

        sourceField = jQuery_pocket("#requestform").find("[name=source]");
        sourceField.removeClass("error");
        sourceString = sourceField.val();
        if (!isUrlValid(sourceString)) {
            sourceField.addClass("error");
            isError = true;
        }

        if (isError) {
            alert("Пожалуйста, заполните данные корректно.");
        }
        else {
            jQuery_pocket.post('/Pocket/Init',
            {
                source: sourceString
            },
            function (data) {
                if (data.Result) {
                    document.location.href = data.Object;
                }
                else {
                    alert(data.Message);
                }
            }, "json");

        }

    });

});

